    void loadDosen() throws RemoteException {
        listDosen = dsnService.getAll();
    }

    void isiListDosen() {
        comboDosen.removeAllItems();
        for (Dosen dsn : listDosen) {
            comboDosen.addItem(dsn.getDosen());
        }
    }

    void kosongkanText() {
        txtKdMk.setText("");
        txtMk.setText("");
        txtJurusan.setText("");
        comboDosen.removeAllItems();
        for (Dosen dsn : listDosen) {
            comboDosen.addItem(dsn.getDosen());
        }
        txtSks.setText("");
    }

    void isiTabelMk() {
        try {
            Object[][] data = new Object[records.size()][5];

            int x = 0;
            for (Mk mk : records) {
                data[x][0] = mk.getKd_mk();
                data[x][1] = mk.getMk();
                data[x][2] = mk.getSks();
                data[x][3] = mk.getJurusan();
                data[x][4] = mk.getKd_dosen();
                x++;
            }
            String[] judul = {"Kode Mk", "Mk", "Sks", "Jurusan", "Kd Dosen"};
            tableMk.setModel(new DefaultTableModel(data, judul));
            jScrollPane1.setViewportView(tableMk);
        } catch (Throwable ex) {
            ex.printStackTrace();
        }
    }

    private void loadRecords() throws RemoteException {
        records = mkService.getAll();
        this.isiTabelMk();
    }

    void isitext() {
        Mk mk = records.get(row);
        txtKdMk.setText(mk.getKd_mk());
        txtMk.setText(mk.getMk());
        txtSks.setText(String.valueOf(mk.getSks()));
        txtJurusan.setText(mk.getJurusan());
        comboDosen.removeAllItems();
        for (Dosen dsn : listDosen) {
            comboDosen.addItem(dsn.getDosen());
        }
    }

    private void statusAwal() throws RemoteException {
        this.loadRecords();
        this.kosongkanText();
        this.loadDosen();
        this.isiListDosen();
    }

