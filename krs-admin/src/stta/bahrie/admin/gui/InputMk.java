    private void inputButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputButtonActionPerformed
        try {
            // TODO add your handling code here:
            Mk mk = new Mk();
            mk.setKd_mk(txtKdMk.getText());
            mk.setMk(txtMk.getText());
            mk.setSks(Integer.valueOf(txtSks.getText()));
            mk.setJurusan(txtJurusan.getText().toUpperCase());
            mk.setKd_dosen(listDosen.get(comboDosen.getSelectedIndex()).getKd_dosen());
            mkService.insert(mk);
            this.statusAwal();
        } catch (RemoteException ex) {
            Logger.getLogger(InputMk.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_inputButtonActionPerformed

